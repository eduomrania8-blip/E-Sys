/**
 * تنظيف النصوص وإزالة المسافات الزائدة وتحويلها للصيغة الموحدة
 */
export function sanitizeSubject(input: string | null | undefined): string {
  if (!input) return '';
  // إزالة المسافات الزائدة من البداية والنهاية والوسط
  let clean = input.trim().replace(/\s+/g, ' ');
  // إزالة التشكيل إذا وجد
  clean = clean.replace(/[\u064B-\u065F]/g, '');
  
  // البحث في القاموس (بناءً على الكلمة بالكامل)
  return SUBJECTS_MAP[clean] || SUBJECTS_MAP[clean.toLowerCase()] || clean;
}

export function sanitizeQualification(input: string | null | undefined): string {
  if (!input) return '';
  let clean = input.trim().replace(/\s+/g, ' ');
  
  // توحيد الكلمات الشائعة داخل النص (مثل: بكالوريوس تجارة بدل بكلريوس تجارة)
  Object.keys(QUALIFICATIONS_MAP).forEach(wrong => {
    if (clean.includes(wrong)) {
      clean = clean.replace(new RegExp(wrong, 'g'), QUALIFICATIONS_MAP[wrong]);
    }
  });
  
  return clean;
}

