/**
 * دالة لحساب العجز والزيادة لمادة معينة في مدرسة معينة
 * المنطق: (فصول الصف × حصص المادة في هذا الصف) = اللازم بالحصص
 * المتوفر = مجموع نصاب كل معلم حسب كادره
 * الفرق = المتوفر - اللازم
 */
export function calculateSubjectShortage(
  subject: string,
  classStats: Array<{ grade_level?: string | null; number_of_classes: number }>,
  teachers: Array<{ cadre_position?: string | null; work_status?: string | null }>,
  stage: 'primary' | 'preparatory' | 'secondary' = 'primary'
) {
  // 1. حساب الاحتياج الفعلي (اللازم بالحصص)
  let requiredPeriods = 0;
  const curriculum = CURRICULUM_BY_STAGE[stage];
  const subjectCurriculum = curriculum[subject] || {};
  
  classStats.forEach(stat => {
    const grade = stat.grade_level || '';
    const periodsForThisGrade = subjectCurriculum[grade] || 0;
    const numClasses = stat.number_of_classes || 0;
    requiredPeriods += (numClasses * periodsForThisGrade);
  });

  // 2. حساب الموجودين بالحصص (نصاب المعلمين الفعليين)
  let availablePeriods = 0;
  
  // المعلمون على رأس العمل فقط (أو غير محدد = يعمل)
  const activeTeachers = teachers.filter(t =>
    !t.work_status || t.work_status === 'على رأس العمل' || t.work_status === 'يعمل'
  );

  activeTeachers.forEach(teacher => {
    const cadre = (teacher.cadre_position || '').trim();
    const cadreQuotas = CADRE_QUOTAS[stage] as Record<string, number>;
    
    // بحث مباشر (تطابق تام)
    let quota = cadreQuotas[cadre];
    if (quota === undefined) {
      // إذا كان الكادر مجهول أو غير موجود في القائمة → 24 حصة (القاعدة الوزارية)
      quota = 24;
    }
    availablePeriods += quota;
  });

  // 3. الفرق والنتيجة
  const netPeriods = availablePeriods - requiredPeriods;
  const baseQuota = CADRE_QUOTAS[stage]['معلم مساعد'] as number;
  const netTeachers = baseQuota > 0 ? netPeriods / baseQuota : 0;

  return {
    requiredPeriods,
    availablePeriods,
    netPeriods,
    netTeachers: Number(netTeachers.toFixed(2)),
    status: netPeriods > 0 ? 'زيادة' : netPeriods < 0 ? 'عجز' : 'متزن',
    activeTeachersCount: activeTeachers.length,
  };
}
