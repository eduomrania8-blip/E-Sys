/**
 * محرك التوحيد القياسي للنصوص العربية v1.0
 * يقوم بمعالجة النصوص لضمان ثباتها برغم اختلاف طرق الكتابة الشائعة.
 */

/**
 * دالة توحيد الحروف العربية (أ إ آ -> ا، ة -> ه، ى -> ي)
 * تستخدم لضمان تطابق الكلمات في البحث والتجميع الإحصائي.
 */
export function normalizeArabic(text: string | null | undefined): string {
  if (!text) return '';

  return text
    .trim()
    // 1. إزالة التشكيل (diacritics)
    .replace(/[\u064B-\u065F]/g, '')
    // 2. توحيد الألف (أ إ آ -> ا)
    .replace(/[أإآ]/g, 'ا')
    // 3. توحيد التاء المربوطة (ة -> ه)
    .replace(/ة/g, 'ه')
    // 4. توحيد الياء (ى -> ي)
    .replace(/ى/g, 'ي')
    // 5. توحيد الهمزة على الياء (ئ -> ي)
    .replace(/ئ/g, 'ي')
    // 6. إزالة المسافات المتكررة
    .replace(/\s+/g, ' ');
}

/**
 * دالة تنظيف الأسماء (تستخدم لأسماء الطلاب والمعلمين)
 */
export function sanitizeName(name: string | null | undefined): string {
  if (!name) return '';
  
  // تنظيف أساسي دون تغيير جذري للحروف (للحفاظ على شكل الاسم)
  // لكن نوحد المسافات ونحذف التشكيل
  return name
    .trim()
    .replace(/[\u064B-\u065F]/g, '')
    .replace(/\s+/g, ' ');
}
